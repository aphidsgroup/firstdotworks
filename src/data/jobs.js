export const formatSalary = (min, max, currency = 'INR') => {
  const fmt = (n) => n >= 100000 ? `${(n / 100000).toFixed(n % 100000 === 0 ? 0 : 1)}L` : `${(n / 1000).toFixed(0)}K`
  return `₹${fmt(min)} – ₹${fmt(max)}`
}
